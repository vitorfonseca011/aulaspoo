//O == significa iguldade entao se o numero for igual vai aparecer 'true' se nao for vai ser 'false'
console.log(5 == 5); //true
//O != significa diferença entao se o numero for diferente vai aparecer 'true' se nao for vai ser 'false'
console.log(5 != 1); //true
//o > ou < é igual na matemática maior ou menor que
console.log(10 > 5); //true
console.log(3 < 5); //true
// >= significa maior igual que, igual na matemática
console.log(7 >= 7); //true
//o && significa 'e' entao se toda operacao estiver certa vai aparecer 'true' e se uma estiver errada vai aparecer 'false'
console.log(5 == 5 && 10 >7); //true
console.log(5 == 4 && 10 > 7); //false
//o || significa 'ou' entao se uma operacao estiver certa e a outra errada vai aparecer 'true'
console.log(5 == 5 || 5 > 7); //true
console.log(5 == 4 || 5 > 7); //false