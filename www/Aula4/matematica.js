function adicao(a, b) {
    var resultado = a + b
    console.log(resultado)
}

function subtracao(x, y) {
    var resultado = x - y
    console.log(resultado)
}

function multiplicar(c, d) {
    var resultado = c * d
    console.log(resultado)
}

function dividir(u, v) {
    var resultado = u / v
    console.log(resultado)
}

subtracao(10, 5)
adicao(10, 5)
multiplicar(10, 5)
dividir(10, 5)
