function dobro(numero) {
    var resultado = numero * 2;
    return resultado
}


var valor = dobro(5);
console.log("O dobro de 5 é: "  + valor);

function triplo(numero) {
    var resultado = numero * 3;
    return resultado
}


var valor = triplo(5);
console.log("O triplo de 5 é: "  + valor);